      <p class="chinesetext">2008年11月1日，随着一篇名为《比特币：一种点对点的电子现金系统》（Bitcoin: A Peer-to-peer Electronic Cash System）的论文在一个不知名的密码学论坛上的发布，“比特币”这一概念第一次登上了历史舞台。哈耶克的“去中心化”货币构想不仅在比特币模型中得到了继承，更因新技术的出现而得到了进一步的发扬光大。依据中本聪论文中所言，比特币是一种完全开源的基于点对点（P2P）算法而实现的电子现金系统，其发行过程并不由任何中央银行或个人单独完成，P2P算法意味着任何组织或个人都可成为比特币发行过程中的一个环节，同时也意味着在发行环节之中，比特币不会被任何组织或个人控制。其同时具有匿名性、便利性及抗通货膨胀性等种种优秀特性。</p>
      <p class="chinesetext">在比特币模型提出之后，借美国金融危机及随后的欧债危机等契机，这种全新的货币模式受到了越来越多投资者及投机者的青睐，同时也有部分比特币的坚定拥护者开始在自己的店铺中支持比特币结算业务。而在欧洲国家芬兰，沉重的债务危机使得芬兰央行也开始宣传比特币支付的便捷性及优越性，这使得比特币在芬兰一时蔚然成风。随后，整条比特币产业链似乎也开始蠢蠢欲动，发行端的“比特币挖矿机”、“比特币挖矿芯片”，客户端的“比特币自动售货机”应运而生。</p>
      <p class="chinesetext">然而，比特币在逐渐风靡全球的过程中也备受公众质疑。首先，比特币的模型本身直接违背了传统货币的定义，比特币本身并非金属货币，由于其去中心化特性，比特币也并不可能成为一种传统意义上的信用货币；其次，比特币的安全性和匿名性并不像中本聪论文中所言那样万无一失，比特币交易平台已被证明是整个系统中最脆弱一环，在比特币发展至今的五年时间里，已有数起黑客入侵比特币交易平台并廉价抛售比特币导致比特币汇率大跌的案例，政府也可通过查封比特币交易平台来控制比特币的生存命脉；再者，比特币的“抗通货膨胀”特性本身反而意味着它并不适合成为一种货币，由于算法规定，比特币的总量将被限定在2100万个，这意味着理论上，它无时无刻不处于通货紧缩的状态。这决定了它更适合成为一种短期的投机产品而并非货币；最后，政府的监管与否无疑成为了事关比特币生死存亡的最关键因素。</p>
      <p class="chinesetext">在汇文中国二〇一四的大舞台上，就特殊委员会的设置而言，没有任何一个话题能比比特币更具有综合性、即时性以及思辨性。以往的特殊委员会往往重形式而轻内容。在会前，主席团往往为追求历史模拟的逼真性而设置极其复杂的场景和人物，并在规则流程上仔细推敲。然而与会过程中，代表们却往往因对规则流程把握的不足以及对表面事物（如特殊的着装）的过分关注而使得整个会议变成了一场华丽的COSPLAY。而在比特币二〇一四会场中，庞大而复杂的背景知识以及即时信息迫使代表不得不更关注会议内容本身，而身为主席，我们的任务是化繁为简，提纲挈领，引领代表在这个集数学、计算机科学、经济学、金融学及政治博弈为一体的领域凌波微步，在会后能有知识和思维等多方面的收获</p>
